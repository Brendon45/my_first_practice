// Declare a variable without initializing it, so it is undefined
let undefinedVariables;

// Log the type of undefinedVariables to the console
console.log("Type of undefinedVariables : ", typeof undefinedVariables); // Output: "undefined"

// Define a function named exampleFunction
function exampleFunction() {
  return "I am a function!";
}

// Log the type of exampleFunction to the console
console.log("Type of exampleFunction : ", typeof exampleFunction); // Output: "function"

// Initialize a variable with the value null
const nullValue = null;

// Log the type of nullValue to the console
console.log("Type of nullValue : ", typeof nullValue); // Output: "object"
